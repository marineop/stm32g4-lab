/**
  * @brief  Function implementing the defaultTask thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_StartDefaultTask */
void StartDefaultTask(void *argument)
{
  /* USER CODE BEGIN 5 */

	spi1.hspi = &hspi1;
	SpiDivice_Initialize(&spi1);

	{
		ADXL345Settings settings;
		ADXL345Settings_Initialize(&settings);

		settings.spi = &spi1;
		settings.stackDepth = ACC_STACK_SIZE;
		settings.stackBuffer = accStack;
		settings.messagesSize = ACC_MESSAGES_SIZE;
		settings.messagesStorageArea = accMessageStorage;
		settings.ssGpio = GPIOA;
		settings.ssPin = GPIO_PIN_4;

		ADXL345_Initialize(&acc, &settings);
	}

	// Read device ID
	uint8_t deviceId = 0;
	ADXL345_ReadDevid(&acc, &deviceId);
	printf("ADXL345 DEVID=%X\r\n", deviceId);

	// Disable all interrupts
	ADXL345_WriteIntEnable(&acc, 0);

	// Clean interrupt flags
	uint8_t intSource = 0;
	ADXL345_ReadIntSource(&acc, &intSource);

	// Enable FULL_RES
	ADXL345_WriteFullRes(&acc, 1);

	// Range=Â±16 g
	ADXL345_WriteRange(&acc, ADXL345Range_16);

	// Set output rate to 12.5 Hz
	ADXL345_WriteOutputRate(&acc, ADXL345OutputRate_6_25);

	// Setup single tap parameters
	ADXL345_WriteThreshTap(&acc, 40); // 62.5 mg * 40 = 2.5 g
	ADXL345_WriteDur(&acc, 32); // 625us * 32 = 20 ms
	ADXL345_WriteTapAxes(&acc, ADXL345TapAxes_X | ADXL345TapAxes_Y | ADXL345TapAxes_Z);

	// All interrupts maps to INT_1
	ADXL345_WriteIntMap(&acc, 0);

	// Enable DATA_READY interrupt
	ADXL345_WriteIntEnable(&acc, ADXL345IntEnable_DataReady | ADXL345IntEnable_SingleTap);

	// start measure
	ADXL345_WriteMeasure(&acc, 1);

	/* Infinite loop */
	int32_t counter = 0;
	for(;;)
	{
		size_t bytesReceived = xMessageBufferReceive(
			acc.messages,
			receiveBuffer,
			RECEIVE_BUFFER_SIZE,
			pdMS_TO_TICKS(1000));

		if(bytesReceived > 0)
		{
			ADXL345Event event = receiveBuffer[0];
			if(event == ADXL345Event_Data)
			{
				++counter;

				int16_t x = *((int16_t*)(receiveBuffer + 1));
				int16_t y = *((int16_t*)(receiveBuffer + 3));
				int16_t z = *((int16_t*)(receiveBuffer + 5));
				printf("%ld: x=%c%ld.%ldg, y=%c%ld.%ldg, z=%c%ld.%ldg\r\n",
					counter,
					GetGSign(x), GetGIntegerPart(x), GetGFractionPart(x),
					GetGSign(y), GetGIntegerPart(y), GetGFractionPart(y),
					GetGSign(z), GetGIntegerPart(z), GetGFractionPart(z));
			}
			else if(event == ADXL345Event_Interrupt)
			{
				uint8_t intSource = receiveBuffer[1];
				if(intSource & ADXL345IntEnable_SingleTap)
				{
					printf("Single Tap\r\n");
				}
			}
		}

	}
  /* USER CODE END 5 */
}

